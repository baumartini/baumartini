codigo = int(input("Introduce el código del frasco (1, 2 o 3): "))

if codigo == 1:
    print("Descripción: Frasco chico.")
elif codigo == 2:
    print("Descripción: Frasco mediano.")
elif codigo == 3:
    print("Descripción: Frasco grande.")
else:
    print("Error: Código no válido. Debe ser 1, 2 o 3.")
    
